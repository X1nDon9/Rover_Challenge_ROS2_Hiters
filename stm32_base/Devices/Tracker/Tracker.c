#include "Tracker.h"

#if DEVICE_TRACKER

/**
  * @brief  读取五路循迹传感器并返回偏差值
  * @note   传感器检测到黑线时输出高电平（返回 1），脱离黑线时输出低电平（返回 0）。
  *         偏差值定义：
  *           0  直走（仅 M 在线）
  *          -1  小左偏（L1、M 在线）
  *          -2  大左偏（仅 L1 在线）
  *          -3  大大左偏（仅 L2 在线）
  *          -4  直角左转（直角，L2/L1/M 均在线）
  *           1  小右偏（M、R1 在线）
  *           2  大右偏（仅 R1 在线）
  *           3  大大右偏（仅 R2 在线）
  *           4  直角右转（直角，M/R1/R2 均在线）
  *         若传感器状态不匹配以上任何一种，返回上一次的有效偏差值。
  * @retval 偏差值（int16_t）
  */
int16_t Tracker_Read(void)
{
    static int16_t track = 0;

    if (L2 == 0 && L1 == 0 && M >= 1 && R1 == 0 && R2 == 0) { /* 仅 M 在线：直走 */
        track = 0;
    } else if (L2 == 0 && L1 >= 1 && M >= 1 && R1 == 0 && R2 == 0) { /* L1+M 在线：小左偏 */
        track = -1;
    } else if (L2 == 0 && L1 >= 1 && M == 0 && R1 == 0 && R2 == 0) { /* 仅 L1 在线：大左偏 */
        track = -2;
    } else if (L2 >= 1 && L1 == 0 && M == 0 && R1 == 0 && R2 == 0) { /* 仅 L2 在线：大大左偏 */
        track = -3;
    } else if (L2 >= 1 && L1 >= 1 && M >= 1) {                       /* M+R1+R2 均在线：左转直角 */
        track = -4;
    } else if (L2 == 0 && L1 == 0 && M >= 1 && R1 >= 1 && R2 == 0) { /* M+R1 在线：小右偏 */
        track = 1;
    } else if (L2 == 0 && L1 == 0 && M == 0 && R1 >= 1 && R2 == 0) { /* 仅 R1 在线：大右偏 */
        track = 2;
    } else if (L2 == 0 && L1 == 0 && M == 0 && R1 == 0 && R2 >= 1) { /* 仅 R2 在线：大大右偏 */
        track = 3;
    } else if (M >= 1 && R2 >= 1 && R1 >= 1) {                       /* L2+L1+M 均在线：右转直角 */
        track = 4;
    }
    /* 其余状态保持上一次有效值，避免抖动导致误判 */

    return track;
}

#endif /* DEVICE_TRACKER */
